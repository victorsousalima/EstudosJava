package ListaExercicios;

public class ex02 {

	public static void main(String[] args) {
		
		double n1 = 14;
		double n2 = 5;
		
		double soma = n1 + n2;
		double sub = n1 - n2;
		double mult = n1 * n2;
		double div = n1 / n2;
		
		System.out.println("O Resultado da SOMA é: " + (int)soma);
		System.out.println("O Resultado da SUBTRAÇÃO é: " + (int)sub);
		System.out.println("O Resultado da MULTIPLICAÇÃO é: " + (int)mult);
		System.out.println("O Resultado da DIVISÃO é: " + div);
		
	}
}
